class NumIslands {
    public int numIslands(char[][] grid) {
        if(grid == null || grid.length == 0){
            return 0;
        }
        int nr = grid.length;
        int nc = grid[0].length;
        int nums = 0;
        for(int i = 0;i < nr;i++){
            for(int j = 0; j < nc;j++){
                if(grid[i][j] == '1'){
                    nums++;
                    dfs(grid, i, j);
                }
            }
        }
        return nums;
    }
    private void dfs(char[][] grid, int i, int j){
        int nr = grid.length;
        int nc = grid[0].length;
        if(i<0 || j < 0 || i >= nr || j >= nc || grid[i][j] == '0'){
            return;
        }
        grid[i][j] = '0';
        dfs(grid, i-1,j);
        dfs(grid, i+1, j);
        dfs(grid,i, j-1);
        dfs(grid,i,j+1);
    }
}
