import java.util.ArrayList;
import java.util.List;

/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class GetMinimumDifference {
    List<Integer> midumSortArr = new ArrayList<Integer>();
    public int getMinimumDifference(TreeNode root) {
        midumSort(root);
        int min = Integer.MAX_VALUE;
        if(midumSortArr.size() <2){
            return 0;
        }
        for(int i = 1; i < midumSortArr.size();i++){
            min = Math.min(min, midumSortArr.get(i) - midumSortArr.get(i-1));
        }
        return min;
    }

    private void midumSort(TreeNode root){
        if(root ==null){
            return;
        }
        midumSort(root.left);
        midumSortArr.add(root.val);
        midumSort(root.right);

    }
    public class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;
        TreeNode() {}
        TreeNode(int val) { this.val = val; }
        TreeNode(int val, TreeNode left, TreeNode right) {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }
}
