/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Partition {
    public ListNode partition(ListNode head, int x) {
        ListNode biggerNodeHead = new ListNode();
        ListNode dummyHead = new ListNode();
        dummyHead.next = head;
        ListNode cur = head, pre = dummyHead;
        ListNode p2 = biggerNodeHead;
        while(cur !=null){
            if(cur.val >= x){
                pre.next = cur.next;
                p2.next = cur;
                cur = cur.next;
                p2 = p2.next;
            }else{
                pre = pre.next;
                cur = cur.next;
            }
        }
        p2.next = null;
        pre.next = biggerNodeHead.next;
        return dummyHead.next;
    }

    static class ListNode {
        int val;
        ListNode next;
        ListNode() {}
        ListNode(int val) { this.val = val; }
        ListNode(int val, ListNode next) { this.val = val; this.next = next; }
    }
}
